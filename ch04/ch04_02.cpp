#include <iostream>
using namespace std;

int main() {
  cout << "21%6 = " << 21 % 6 << endl;
  cout << "21%7 = " << 21 % 7 << endl;
  cout << "-21%-8 = " << -21 % -8 << endl;
  cout << "21%-5 = " << 21 % -5 << endl;
  cout << "21/6 = " << 21 / 6 << endl;
  cout << "21/7 = " << 21 / 6 << endl;
  cout << "-21/-8 = " << -21 / -8 << endl;
  cout << "21/-5 = " << 21 / -5 << endl;
  return 0;
}

